                    <h1>Mi az a UX Design és mit kell tudni róla?</h1>
                    <h3>Mi az az UX design?</h3>
                    <p><b>A UX design-ra nincs általános, közösen elfogadott definíció.</b> A UX design egy koncepció, aminek több dimenziója van és magába foglal rengeteg különböző tudományágat – mint például az interakció designt, az információ építészetet, a vizuális designt, a használhatóságot és ember-számítógép közti interakciót. A következő fejezetben megpróbálok világosabb képet alkotni arról, hogy ez pontosan mit is jelent.</p>
                    <h3>Az UX design definíciója</h3>
                    <p>A UX Design olyan termékek létrehozásának folyamata, amelyek a hasznosságon túl személyes élményt is nyújtanak használóiknak. Az UX design-ba beletartozik a termék használhatósága és az az élmény is, amely a használat során ér minket. A termék megszerzésének és integrálásának teljes folyamatát is magában foglalja, beleértve a márkaépítéssel, tervezéssel, használhatósággal és funkcióval kapcsolatos szempontokat.</p>
                    <p>Azokat a termékeket, amelyek jó felhasználó élményt nyújtanak (pl: az iPhone) úgy tervezik , hogy nem csak a termék használatát tartják szem előtt, hanem az egész megszerzési folyamatot, a birtoklást és a hibaelhárítást is figyelembe veszik.</p>
                    <p>Az UX design fontos része az a folyamat, amelynek során a fogyasztók élményt alkotnak. Amikor először találkoznak a termékkel, a fogyasztó pillanatnyi véleményt alkot, ami idővel - tipikusan a termék használata során - folyamatosan változik. Ebben a folyamatban a fogyasztó élményei, cselekedetei, motivációja és megismerése kombinálja és alkotja a felejthetetlen és összefüggő történetet: a „user experience”-t. Ez a folyamat érzelmi reakciókat vált ki, melyek nagymértékben befolyásolják azt, hogy az élmény pozitív vagy negatív lesz.</p>
                    <p>Az UX tervezők munkájuk során folyamatosan a felhasználói élmény javítására törekednek. Ahhoz, hogy ez sikerüljön az UX tervezőnek figyelembe kell vennie a termékkel kapcsolatban a <b>mit</b>, a <b>miért</b>-et és <b>hogyan</b>-t.</p>
                    <p>A <b>Miért</b> arra keres választ, hogy a fogyasztó miért választotta az adott terméket. Azért, mert egy adott feladatot akar végrehajtani vele, vagy azért, mert birtoklásának és használatának értékét részesíti előnyben. Ismerős ugye, mikor valaki pusztán azért vásárol meg egy terméket, mert a birtoklása örömet okoz? A <b>Mit</b> azt fejezi ki, amit a termékkel végezni lehet, tehát a funkcionalitását. A <b>Hogyan</b> pedig használhatósági és esztétikai szemszögből vizsgálja a termék funkciójának tervezését. UX design a miért-tel kezdődik, utána meghatározza a <b>Mit</b>, végül pedig választ ad a <b>Hogyan</b>-ra annak érdekében, hogy olyan termékeket alkossanak, amelyeket élvezet használni.</p>
